    private void insertBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_insertBtnMouseClicked

        if (ticketNoField.getText().isBlank() == true || departureField.getText().isBlank() || statusField.getText().isBlank() || bookingField.getText().isBlank()
                || adminNoField.getText().isBlank()){
             JOptionPane.showMessageDialog(null, "Some data fields are empty", "ERROR" , JOptionPane.ERROR_MESSAGE);
        }
        else{
        try {          
            String sql = "INSERT INTO `tickets`(`TicketNo`, `Type`, `Reservation_Status`, `BookingID`, `AdminNo`) VALUES (?,?,?,?,?)"; 
            con = DriverManager.getConnection("jdbc:mysql://localhost/skyscanner","root","");
            pst = con.prepareStatement(sql);
            pst.setString(1, ticketNoField.getText());
            pst.setString(2, departureField.getText());
            pst.setString(3, statusField.getText());
            pst.setString(4, bookingField.getText());
            pst.setString(5, adminNoField.getText());
            pst.executeUpdate();
            
                       
            JOptionPane.showMessageDialog(null , "Data inserted successfully");
            
        } catch (Exception ex){
            JOptionPane.showMessageDialog(null, "Error in inserting data" , "ERROR" , JOptionPane.ERROR_MESSAGE);
        }
        showTableData();
        }
        
    }//GEN-LAST:event_insertBtnMouseClicked

