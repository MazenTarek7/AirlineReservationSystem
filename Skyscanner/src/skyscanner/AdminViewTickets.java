    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        if (departureField.getText().isBlank() == true  || statusField.getText().isBlank() == true || passportNoField.getText().isBlank() == true || 
               adminNoField.getText().isBlank() == true || ticketNoField.getText().isBlank() == true ){
             JOptionPane.showMessageDialog(null, "Some data fields are empty , please fill them and retry" , "ERROR" , JOptionPane.ERROR_MESSAGE);
        }
        
        
        try {
        String sql = "UPDATE `tickets` SET `Type`= ? ,`Reservation_Status`= ? ,`PassportNo`= ? ,`AdminNo`= ? WHERE TicketNo = ?";
        con = DriverManager.getConnection("jdbc:mysql://localhost/skyscanner","root","");
        pst = con.prepareStatement(sql);
        pst.setString(1, departureField.getText());
        pst.setString(2, statusField.getText());
        pst.setString(3, passportNoField.getText());
        pst.setString(4, adminNoField.getText());
        pst.setString(5, ticketNoField.getText());
        pst.executeUpdate();
        
        
        
        JOptionPane.showMessageDialog(null , "Data updated successfully");
        
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Error in updating data" , "ERROR" , JOptionPane.ERROR_MESSAGE);
        }
        showTableData();
        

    }//GEN-LAST:event_updateBtnActionPerformed

    private void deleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteBtnMouseClicked
        if (ticketNoField.getText().isBlank() == true){
            JOptionPane.showMessageDialog(null, "Ticket Number field is empty , it must be filled to delete ticket" , "ERROR" , JOptionPane.ERROR_MESSAGE);
        }
        try {
            String sql = "DELETE FROM tickets where TicketNo = ?";
            con = DriverManager.getConnection("jdbc:mysql://localhost/skyscanner","root","");
            pst = con.prepareStatement(sql);
            pst.setString(1, ticketNoField.getText());
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(null , "Data deleted successfully");
            
        } catch (Exception ex){
            JOptionPane.showMessageDialog(null, "Error in deleting data" , "ERROR" , JOptionPane.ERROR_MESSAGE);
        }
        showTableData();

    }//GEN-LAST:event_deleteBtnMouseClicked

