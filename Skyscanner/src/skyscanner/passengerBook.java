    private void bookBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookBtnActionPerformed
        if (ticketNoField.getText().isBlank() == true){
            JOptionPane.showMessageDialog(null, "Ticket Number field must be filled" , "ERROR" , JOptionPane.ERROR_MESSAGE);
        }
        else{
        try {
            String sql = "UPDATE `tickets` SET `PassportNo`= ? WHERE TicketNo = ?";
            con = DriverManager.getConnection("jdbc:mysql://localhost/skyscanner","root","");
            pst = con.prepareStatement(sql);
            pst.setString(1, passportNum);
            pst.setString(2, ticketNoField.getText());
            pst.executeUpdate();

            JOptionPane.showMessageDialog(null , "Data updated successfully");

        } catch (Exception ex){
            JOptionPane.showMessageDialog(null, "Error in updating data" , "ERROR" , JOptionPane.ERROR_MESSAGE);
        }
        showTableData();
        }
    }//GEN-LAST:event_bookBtnActionPerformed

