    public void showTableData(){
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost/skyscanner","root","");
            String sql = "SELECT tickets.TicketNo , tickets.Type , flightschedule.Flight_Date , flight.Price from tickets\n" +
"inner join flightschedule on tickets.TicketNo = flightschedule.Tno inner join flight on flightschedule.ScheduleID = flight.ScheduleID\n" +
"where tickets.PassportNo is NULL";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            Table.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception ex){
            JOptionPane.showMessageDialog(null, "Error in displaying data" , "ERROR" , JOptionPane.ERROR_MESSAGE);
        }
    
    }
    
    
    
