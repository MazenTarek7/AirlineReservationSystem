    private void insertBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_insertBtnMouseClicked
        if (staffNoField.getText().isBlank() == true || fnameField.getText().isBlank() == true|| lnameField.getText().isBlank() == true
            || dobField.getText().isBlank() == true || cityField.getText().isBlank() == true || countryField.getText().isBlank() == true
            || genderField.getText().isBlank() == true || jobTypeField.getText().isBlank() == true ||  phoneField.getText().isBlank() == true){
            JOptionPane.showMessageDialog(null, "Some data fields are empty"
                , "ERROR" , JOptionPane.ERROR_MESSAGE);
        }
        else{
            try {
                String sql = "INSERT INTO `staff`(`StaffNo`, `Fname`, `Lname`, `DOB`, `City`, `Country`, `Gender`, `Job_Type`) VALUES (?,?,?,?,?,?,?,?)";
                con = DriverManager.getConnection("jdbc:mysql://localhost/skyscanner","root","");
                pst = con.prepareStatement(sql);
                pst.setString(1, staffNoField.getText());
                pst.setString(2, fnameField.getText());
                pst.setString(3, lnameField.getText());
                pst.setString(4, dobField.getText());
                pst.setString(5, cityField.getText());
                pst.setString(6, countryField.getText());
                pst.setString(7, genderField.getText());
                pst.setString(8, jobTypeField.getText());
                pst.executeUpdate();

                String sql2 = "INSERT INTO `staff_phone`(`StaffNo`, `PhoneNo`) VALUES (?,?)";
                pst = con.prepareStatement(sql2);
                pst.setString(1, staffNoField.getText());
                pst.setString(2, phoneField.getText());
                pst.executeUpdate();
                
                JOptionPane.showMessageDialog(null , "Data inserted successfully");

            } catch (Exception ex){
                JOptionPane.showMessageDialog(null, "Error in inserting data , StaffNo" , "ERROR" , JOptionPane.ERROR_MESSAGE);
            }
            showTableData();
        }
    }//GEN-LAST:event_insertBtnMouseClicked

