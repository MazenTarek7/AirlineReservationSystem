    public void showTableData(){
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost/skyscanner","root","");
            String sql = "select staff.StaffNo , staff.Fname , staff.Lname , staff.DOB , staff.City , staff.Country , staff.Gender , staff.Job_Type , staff_phone.PhoneNo from staff\n" +
"join staff_phone on staff.StaffNo = staff_phone.StaffNo\n" +
"where Job_Type <>'ADMIN';";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            Table.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception ex){
            JOptionPane.showMessageDialog(null, "Error in displaying data" , "ERROR" , JOptionPane.ERROR_MESSAGE);
          }
    
    }
    
    
    
