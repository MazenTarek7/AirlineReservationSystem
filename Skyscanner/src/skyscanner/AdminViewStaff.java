    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed

        if (fnameField.getText().isBlank() == true|| lnameField.getText().isBlank() == true|| dobField.getText().isBlank() == true || 
                cityField.getText().isBlank() == true || countryField.getText().isBlank() == true
            || genderField.getText().isBlank() == true || jobTypeField.getText().isBlank() == true ||  phoneField.getText().isBlank() == true){
            JOptionPane.showMessageDialog(null, "Some fields are empty excluding StaffNo field" , "ERROR" , JOptionPane.ERROR_MESSAGE);
        }
        else{
        try {
            String sql = "UPDATE `staff` SET `Fname`= ?,`Lname`=?,`DOB`=?,`City`=?,`Country`=?,`Gender`=?,`Job_Type`= ? WHERE StaffNo = ?";
            con = DriverManager.getConnection("jdbc:mysql://localhost/skyscanner","root","");
            pst = con.prepareStatement(sql);
            pst.setString(1, fnameField.getText());
            pst.setString(2, lnameField.getText());
            pst.setString(3, dobField.getText());
            pst.setString(4, cityField.getText());
            pst.setString(5, countryField.getText());
            pst.setString(6, genderField.getText());
            pst.setString(7, jobTypeField.getText());
            pst.setString(8, staffNoField.getText());
            pst.executeUpdate();

            
            String sql2 = "UPDATE `staff_phone` SET `PhoneNo`= ? WHERE staff_phone.StaffNo = ?";
            pst = con.prepareStatement(sql2);
            pst.setString(1, phoneField.getText());
            pst.setString(2, staffNoField.getText());
            pst.executeUpdate();

            JOptionPane.showMessageDialog(null , "Data updated successfully");

        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Error in updating data" , "ERROR" , JOptionPane.ERROR_MESSAGE);
        }
        showTableData();
        }

    }//GEN-LAST:event_updateBtnActionPerformed

    private void deleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteBtnMouseClicked
        if (staffNoField.getText().isBlank() == true){
            JOptionPane.showMessageDialog(null, "Staff Number field must be filled to delete data" , "ERROR" , JOptionPane.ERROR_MESSAGE);
        }
        else{
        try {
            String sql = "DELETE FROM staff WHERE StaffNo = ?";
            con = DriverManager.getConnection("jdbc:mysql://localhost/skyscanner","root","");
            pst = con.prepareStatement(sql);
            pst.setString(1, staffNoField.getText());
            pst.executeUpdate();

            String sql2 = "DELETE FROM `staff_phone` WHERE StaffNo = ?";
            pst = con.prepareStatement(sql2);
            pst.setString(1, staffNoField.getText());
            pst.executeUpdate();

            JOptionPane.showMessageDialog(null , "Data deleted successfully");

        } catch (Exception ex){
            JOptionPane.showMessageDialog(null, "Error in deleting data" , "ERROR" , JOptionPane.ERROR_MESSAGE);
        }
        showTableData();
        }
    }//GEN-LAST:event_deleteBtnMouseClicked

